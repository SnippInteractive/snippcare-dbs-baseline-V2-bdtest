    [MinimumThresholdForPool]        DECIMAL (37)  NULL,
    [DefaultNumberOfDevicesToCreate] DECIMAL (37)  NULL,
    [UseExternal]                    BIT           CONSTRAINT [DF_DeviceNumberGeneratorTemplate_External] DEFAULT ((0)) NULL,
